import Foundation
import UIKit

extension UIColor {
    
    static var projectBlack : UIColor {
           get {
               return UIColor(named: "projectBlack")!
           }
    }
    
    static var projectGreen : UIColor {
        get {
            return UIColor(named: "projectGreen")!
        }
    }
    
    static var projectRed : UIColor {
        get {
            return UIColor(named: "projectRed")!
        }
    }
    
    static var projectPurple : UIColor {
        get {
            return UIColor(named: "projectPurple")!
        }
    }
    
    static var projectLightGray : UIColor {
        get {
            return UIColor(named: "projectLightGray")!
        }
    }
}
